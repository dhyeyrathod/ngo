            <br>
            <strong><u>Publication of 'Purush Spandana' (Male Vibrations)</u></strong> <br>
             MAVA brings out Purush Spandana (meaning male  vibrations) - an annual magazine in Marathi. Published since 1996, the magazine  contains short stories, first -person accounts, interviews, poems, analytical  articles and media-trends that voice sentiments of men. The subjects cover a  vast canvas of contemporary issues ranging from misuse of sex-determination  tests, dowry system, woman sexuality and male psyche. The writers hail from  varied professions. The said magazine is the first systematic effort by men to publish issues concerning men and women. Attempting to provide a platform (exclusively) for men, of varied age-groups and backgrounds, to spontaneously express their innermost thoughts on personal lives, upbringing, man-woman relationship, companionship, beauty, friendship, aspirations, vices, violence and various gender-related contemporary issues, the magazine provides fresh insights in understanding men's attitudes.
           <br>
            <br>
            <strong><u>Addressing Specific issues </u></strong> <br>
            In 1995, when a 19  year-old college girl Dipti Khanna was badly burnt by an acid attack in 1995,  MAVA raised over Rs. 75,000/- by appealing to well- wishers and members of the  public to contribute towards Dipti's medical treatment expenses. Most touching was the gesture of prisoners from Nashik Central Jail who sent Rs.11,000, putting in their savings. The underlying  issue behind the attack - the perpetrator's non-acceptance of a woman's  negative response, was discussed by MAVA activists at various levels.  Similarly, the issues of jilted love resulting in heinous forms of violence on  girls in various parts of Maharashtra and the public's apathy to these  incidents have been raised in a big way by MAVA in the last 7 yrs. On the  occasion of 8th March- International Women's Day, every year, MAVA organizes  symposia, workshops on contemporary gender issues like Section 498 A and  Domestic Violence, Misuse of Sex-Determination Tests, Child Sexual Abuse and  Sexual Harassment at the workplace . Men from the field of art and culture are  invited for the programmes to express their concerns and solidarity on gender  issues.<br><br>
            
