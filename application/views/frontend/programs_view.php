            <strong><u>Gender Sensitization Programmes</u></strong> <br>
            Mass awareness programmes on gender issues for different target groups like school and college youth, community mandals, voluntary bodies, residential colonies and employees of business organizations are organized by MAVA. This is done by using different, innovative media like interactive workshop sessions, games, street-plays, essay and poster-competitions, wall- newspapers, radio-plays, poetry reading sessions, talks and discussions by veterans in the field. For the past 8 years, MAVA has been organizing special initiatives for young men on ‘Gender and Masculinity.’ The pioneering initiative, which began in Pune in 2006, has been spread to youths in over 6 other districts of Maharashtra. For details, see Information on Yuva Maîtri. <br>
