                                <div class="content">
                                    <p>Gender-based violence against women is, currently, a serious public health and human rights issue in India. Traditional efforts to tackle gender-based violence against women have concentrated on empowering women to assert themselves. This approach insulates men from the process of transformation and furthers the gender divide. </p>
                                    <p>Men, often become violent, aggressive, and uncaring due to patriarchal modes of socialization that moulds their psyche. Images of masculinity in society are linked to being strong and violent, and to notions that men with ‘power’ are ‘real men’. The situation necessitates efforts that address how men can analyze perceptions of masculinity and create appropriate alternatives. There is a woeful dearth of safe platforms to talk about problems that give rise to violent behavior, including those relating to issues of gender and sexuality. There is also an equal need for positive role models among men, who assert a gender sensitive society and can engage young men in the discourse.</p>
                                    <p>Thus, to address the root cause of the problem, focused efforts promoting men’s involvement (simultaneously with women’s empowerment) are required at various levels. Men have to be involved not as supporters (or do-gooders) but as ‘partners’ and ‘stakeholders.’ And they would be seriously involved only if they are convinced that the problem affects them equally, that it is a problem of both the genders.</p>
                                    <p>MAVA has been addressing the issue, by engaging men - at curative and preventive level, for over 22 years, using varied media and strategies evolved by the organization’s core team. MAVA’s pioneering initiatives in India have been shaping a men’s movement that is deconstructing masculinity and replacing the patriarchal value system (that dis-empowers women) with an egalitarian alternative that empowers girls / women in society and improves their quality of life.</p>
                                    <p>While men have been earlier involved actively in numerous programs for women’s empowerment across the country (in fact, women’s movement in Maharashtra state was boosted by efforts of men like Mahatma Jotiba Phule and R.D.Karve), there were no platforms to examine ‘gender issues’ as  equally ‘men’s issues’. Much before bodies like UN emphasized the need for men’s involvement to improve women’s reproductive health in 1994 (Meet at Cairo), MAVA in India had initiated a thought process in 1991 among men to go into the root cause of gender-based violence and promoted direct intervention programs involving men in the community, to be ‘part of the solution’ (Men seen largely by others concerned as merely ‘part of the problem’).</p>
